let age = 18
if (age <= 2){
    console.log("Infant")
} else if (age <= 4){
    console.log("Toddler")
} else if (age <= 10){
    console.log("Child")
} else if (age <= 12){
    console.log("Preteen")
} else if (age <= 19){
    console.log("Teen")
} else if (age = 20){
    console.log("Young Adult")
} else {
    console.log("You're Old!")
}
