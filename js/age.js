let age = 1;

if (age < 2) {
    console.log('infant')
}
else if (age > 2 && age < 4) {
    console.log('toddler')
}
else if (age > 4 && age < 9) {
    console.log('child')
}
else if (age > 9 && age < 13) {
    console.log('preteen')
}
else if (age > 13 && age < 18) {
    console.log('teen')
}
else if (age > 18 && age < 20) {
    console.log('young adult')
}